export interface ContextAssembledEvent {
  ts: string;
  type: "context.assembled";
  runId: string;
  sources: ContextAssembledSource[];
  excluded: ContextAssembledSource[];
  totalTokens: number;
  modelMaxContext?: number;
  headroomTokens?: number;
}

/**
 * Recorded when the runtime folds the oldest turns of a long conversation into
 * a single summary to bound context growth. The event is appended after all the
 * turns it summarizes; `compactedThroughTs` is the boundary — on replay, every
 * event with `ts < compactedThroughTs` is represented by `summary`, and turns at
 * or after it replay verbatim. Multiple compactions can accumulate; reconstruction
 * honors only the most recent (its summary already subsumes the earlier ones).
 * See `conversation/compaction.ts`.
 */
export interface HistoryCompactedEvent {
  ts: string;
  type: "history.compacted";
  /** Natural-language summary of every turn before `compactedThroughTs`. */
  summary: string;
  /** Boundary: events strictly before this timestamp are summarized. */
  compactedThroughTs: string;
  /** How many reconstructed messages the summary replaced (telemetry). */
  summarizedMessageCount: number;
}

/**
 * A curated connector-skill overlay surfaced into the conversation for the
 * first time, triggered by a matching connector tool call during a run.
 * The reconstructor turns this event into a synthetic assistant message whose
 * body is the overlay wrapped in `<connector-skill>` containment, carrying
 * `metadata.synthetic = "connector_skill_injected"` + `metadata.skill` so the
 * guidance rides the append-only history from the next turn on AND so the
 * engine can detect it on replay to never re-inject. Emitted at most once per
 * (conversation, skill).
 */
export interface ConnectorSkillInjectedEvent {
  ts: string;
  type: "connector.skill.injected";
  /** The connector tool call that triggered the surfacing (e.g. `gmail__send`). */
  toolName: string;
  /** The overlay's skill name (matches the materialized manifest `name`). */
  skillName: string;
  /** The overlay body (markdown), surfaced verbatim into history. */
  skillBody: string;
  /** Scope label for containment / telemetry. Always `"connector"` in v1. */
  scope: string;
}

/**
 * Token usage for a forked model call that is NOT a conversation turn — the
 * compaction summarizer and the auto-title generator. These run the `fast`
 * slot outside the agentic loop and so emit no `llm.response`; without this
 * event their cost is invisible to the usage aggregator. It carries no
 * content (the produced text lands elsewhere — the summary seed, the title),
 * and reconstruction skips it, so it never becomes a message.
 */
export interface AuxUsageEvent {
  ts: string;
  type: "aux.usage";
  /** Which forked call produced this usage. */
  source: "compaction" | "title" | "briefing";
  model: string;
  usage: TokenUsage;
  llmMs: number;
}

/** Discriminated union of all conversation event types. */
export type ConversationEvent =
  | UserMessageEvent
  | RunStartEvent
  | LlmResponseEvent
  | ToolStartEvent
  | ToolDoneEvent
  | ToolProgressEvent
  | RunDoneEvent
  | RunErrorEvent
  | SkillsLoadedEvent
  | ContextAssembledEvent
  | ConnectorSkillInjectedEvent
  | TitleChangeEvent
  | HistoryCompactedEvent
  | AuxUsageEvent;
