/**
 * Conversation IDs are `conv_` followed by exactly 16 lowercase hex characters.
 * Generated by: `conv_${crypto.randomUUID().replace(/-/g, "").slice(0, 16)}`
 */
export const CONVERSATION_ID_RE = /^conv_[a-f0-9]{16}$/;

/**
 * Validate a conversation ID and resolve its file path safely.
 * Throws if the ID format is invalid or the resolved path escapes the base directory.
 */
export function validateConversationId(id: string, dir: string): void {
  if (!CONVERSATION_ID_RE.test(id)) {
    throw new Error(`Invalid conversation ID: must match conv_ + 16 hex chars`);
  }
  const resolved = resolve(dir, `${id}.jsonl`);
  if (!resolved.startsWith(resolve(dir))) {
    throw new Error("Path traversal detected");
  }
}

/**
 * Access context for filtering conversations by user identity.
 *
 * Stage 1: single-owner only. A conversation is accessible iff the
 * caller's `userId` matches the conversation's `ownerId`.
 *
 * Stage 4 reintroduces sharing primitives with policy gating and will
 * extend this context (workspace-admin override, sharing claims, etc.).
 * Defining them now would be a no-op slot — the type tracks what
 * Stage 1 actually uses.
 */
export interface ConversationAccessContext {
  userId: string;
}

/** Options for listing conversations. */
export interface ListOptions {
  limit?: number;
  cursor?: string;
  search?: string;
  sortBy?: "createdAt" | "updatedAt";
  /**
   * Restrict the listing to one room. The room-scoped conversation view (a
   * single `workspaces/<wsId>/conversations/` subtree); omit for the owner's
   * "All rooms" view across every workspace they belong to. Orthogonal to
   * `access` — `workspaceId` is the path filter, ownership is the access gate.
   */
  workspaceId?: string;
}

/** Paginated conversation list result. */
export interface ConversationListResult {
  conversations: ConversationSummary[];
  nextCursor: string | null;
  totalCount: number;
}

/**
 * Options for creating a new conversation.
 *
 * `ownerId` is required — the conversation is owned by exactly one user.
 * `workspaceId` is the room the chat is born in (the binding moment is the
 * first message); it determines the storage directory and is fixed for the
 * conversation's life — there is no mid-chat room switching. When `automationId`
 * is set the conversation is an automation run and lands in that room's
 * `_runs/<automationId>/` partition instead of the owner partition.
 */
export interface CreateConversationOptions {
  workspaceId?: string;
  ownerId: string;
  /** When set, this is an automation run — stored under `_runs/<automationId>/`. */
  automationId?: string;
  metadata?: Record<string, unknown>;
  /** Create with a specific id instead of a generated one. Used by the
   *  detached-turn path so the conversation id is known to the caller before
   *  the engine run starts. */
  id?: string;
}

/** Fields that can be updated on a conversation. */
export interface ConversationPatch {
  title?: string;
}

export interface ConversationSummary {
  id: string;
  createdAt: string;
  updatedAt: string;
  title: string | null;
  messageCount: number;
  preview: string;
  totalInputTokens: number;
  totalOutputTokens: number;
  totalCostUsd: number;
  /**
   * Owner of the conversation. Required: Stage 1 invariant says every
   * conversation has an owner. Files lacking `ownerId` are excluded
   * from the index entirely (see `parseFileHeader`), and direct
   * `load()` calls on such files throw.
   */
  ownerId: string;
  workspaceId?: string;
}

export interface ConversationStore {
  create(options: CreateConversationOptions): Promise<Conversation>;
  load(id: string, access?: ConversationAccessContext): Promise<Conversation | null>;
  append(conversation: Conversation, message: StoredMessage): Promise<void>;
  history(conversation: Conversation, limit?: number): Promise<StoredMessage[]>;
  /**
   * Append a raw conversation event to the stream. Event-sourced stores only —
   * absent on message-based stores (legacy JSONL, in-memory), so callers must
   * feature-detect. Used for events the engine-event sink doesn't produce, e.g.
   * `history.compacted` written by the compaction path.
   */
  appendEvent?(id: string, event: ConversationEvent): void;
  list(options?: ListOptions, access?: ConversationAccessContext): Promise<ConversationListResult>;
  /**
   * Delete a conversation and its backing store. Returns true if the
   * conversation existed AND the caller is allowed to delete it. When
   * `access` is supplied, a non-owner gets `false` (same shape as "not
   * found") — the caller can't distinguish existence-but-not-yours from
   * doesn't-exist, which is the right posture at the store layer.
   */
  delete(id: string, access?: ConversationAccessContext): Promise<boolean>;
  /**
   * Update conversation metadata. Returns the updated conversation, or
   * `null` if the conversation doesn't exist OR — when `access` is
   * supplied — the caller isn't the owner.
   */
  update(
    id: string,
    patch: ConversationPatch,
    access?: ConversationAccessContext,
  ): Promise<Conversation | null>;
  /**
   * Fork a conversation, optionally truncating at a specific message
   * index. Returns the new conversation, or `null` if the source doesn't
   * exist OR — when `access` is supplied — the caller isn't the owner.
   */
  fork(
    id: string,
    atMessage?: number,
    access?: ConversationAccessContext,
  ): Promise<Conversation | null>;
}

/**
 * Conversation metadata — stored as line 1 of the JSONL file.
 *
 * Stage 1: single-owner. The conversation belongs to exactly one user
 * (`ownerId`); workspace is a tool-scoping concern, not an ownership
 * one. Sharing and multi-participant semantics are deferred to Stage 4+
 * with explicit policy gates; the previous `visibility` / `participants`
 * fields are gone.
 *
 * No token totals or cost — those are derived from events at read time
 * (see deriveUsageMetrics in event-reconstructor.ts and the index cache).
 * Storing them invited drift bugs where the cached total didn't match
 * what re-aggregating the events would produce.
 */
export interface Conversation {
  id: string;
  createdAt: string;
  updatedAt: string;
  title: string | null;
  lastModel: string | null;
  /** User who owns this conversation. The single authorization principal. */
  ownerId: string;
  /**
   * The room this conversation lives in — the binding. Set at create from the
   * first-message room and never mutated (no mid-chat room switching). The
   * conversation is stored under `workspaces/<workspaceId>/conversations/`, so
   * the path is authoritative and this field is the denormalised convenience.
   * The session is walled to this one workspace plus the owner's identity tools.
   * Optional only for legacy records predating the room-owned layout.
   */
  workspaceId?: string;
  /** Arbitrary caller-provided metadata. Stored in JSONL first line, never validated. */
  metadata?: Record<string, unknown>;
  /** File format discriminator. "events" for event-sourced files. Absent for legacy message format. */
  format?: "events";
}

/**
 * MCP `resource_link` content block — references a workspace resource
 * (typically `files://<id>`) by URI. The bytes live in the resource's
 * owning store; the conversation log carries only the link.
 *
 * The runtime rehydrates image links to AI SDK V3 `file` parts at the
 * `model.doStream` boundary so vision content reaches the model without
 * the JSONL ever holding raw bytes.
 */
export interface ResourceLinkContentPart {
  type: "resource_link";
  uri: string;
  mimeType: string;
  name: string;
}

/**
 * User-message content shape — text plus MCP `resource_link` blocks.
 * Narrower than `LanguageModelV3UserMessage["content"]` (which also
 * permits inline file parts): callers persist references, not bytes.
 */
export type UserContentPart = LanguageModelV3TextPart | ResourceLinkContentPart;

interface StoredMessageMetadata {
  skill?: string | null;
  /**
   * Marks a message the reconstructor synthesized rather than one the model
   * or user produced. `"connector_skill_injected"` is the surface-once
   * connector-overlay message (paired with `skill`, the overlay name) — the
   * engine inspects this on replay to dedup re-injection. See
   * `event-reconstructor.ts`.
   */
  synthetic?: string;
  toolCalls?: Array<{
    id: string;
    name: string;
    input: Record<string, unknown>;
    output: string;
    ok: boolean;
    ms: number;
    resourceUri?: string;
    resourceLinks?: Array<{
      uri: string;
      name?: string;
      mimeType?: string;
      description?: string;
    }>;
  }>;
  /** Cumulative usage for the assistant turn. Cost is derived at read time. */
  usage?: TokenUsage;
  model?: string;
  llmMs?: number;
  iterations?: number;
  /** Per-call finish reason — surfaces length truncation etc. to the UI. */
  finishReason?: "stop" | "length" | "content-filter" | "tool-calls" | "error" | "other";
  files?: Array<{
    id: string;
    filename: string;
    mimeType: string;
    size: number;
    extracted: boolean;
  }>;
}

interface StoredMessageExtras {
  timestamp: string;
  /** User who sent this message (for multi-user conversations). */
  userId?: string;
  metadata?: StoredMessageMetadata;
}

/**
 * In-memory message shape used by the runtime and event store.
 *
 * For non-user roles, this is `LanguageModelV3Message` plus the platform's
 * timestamp/metadata extras. For user role we widen the content union to
 * include `resource_link` blocks so attached images survive end-to-end as
 * URI references; the runtime rehydrates them to AI SDK `file` parts before
 * calling the model.
 */
export type StoredMessage = StoredMessageExtras &
  (
    | { role: "user"; content: UserContentPart[]; providerOptions?: SharedV3ProviderOptions }
    | Exclude<LanguageModelV3Message, { role: "user" }>
  );

